package VEICULO;

public class Veiculos {

private int CodVeiculos;    
private String Placa;
private String Marca;
private String Modelo;
private String AnoFabricação;
private String Cor;
private String TipoCombustivel;
private int NumeroChassi;
private String Quilometragem;
private String DataUltimaRevisão;

    public int getCodVeiculos() {
        return CodVeiculos;
    }

    public void setCodVeiculos(int CodVeiculos) {
        this.CodVeiculos = CodVeiculos;
    }

    public String getPlaca() {
        return Placa;
    }

    public void setPlaca(String Placa) {
        this.Placa = Placa;
    }

    public String getMarca() {
        return Marca;
    }

    public void setMarca(String Marca) {
        this.Marca = Marca;
    }

    public String getModelo() {
        return Modelo;
    }

    public void setModelo(String Modelo) {
        this.Modelo = Modelo;
    }

    public String getAnoFabricação() {
        return AnoFabricação;
    }

    public void setAnoFabricação(String AnoFabricação) {
        this.AnoFabricação = AnoFabricação;
    }

    public String getCor() {
        return Cor;
    }

    public void setCor(String Cor) {
        this.Cor = Cor;
    }

    public String getTipoCombustivel() {
        return TipoCombustivel;
    }

    public void setTipoCombustivel(String TipoCombustivel) {
        this.TipoCombustivel = TipoCombustivel;
    }

    public int getNumeroChassi() {
        return NumeroChassi;
    }

    public void setNumeroChassi(int NumeroChassi) {
        this.NumeroChassi = NumeroChassi;
    }

    public String getQuilometragem() {
        return Quilometragem;
    }

    public void setQuilometragem(String Quilometragem) {
        this.Quilometragem = Quilometragem;
    }

    public String getDataUltimaRevisão() {
        return DataUltimaRevisão;
    }

    public void setDataUltimaRevisão(String DataUltimaRevisão) {
        this.DataUltimaRevisão = DataUltimaRevisão;
    }

    
}
