package ALUNO;

public class Alunos {

 private int CodAlunos;   
 private String Nome;
 private String CPF;
 private String Endereco;
 private String Telefone;
 private String Email;
 private String DataNasci;
 private String SerieAno;
 private String NomeRespo;
 private String EscolaOrigem;
 
 
    public int getCodAlunos() {
        return CodAlunos;
    }

    public void setCodAlunos(int CodAlunos) {
        this.CodAlunos = CodAlunos;
    }

    public String getNome() {
        return Nome;
    }

    public void setNome(String Nome) {
        this.Nome = Nome;
    }

    public String getCPF() {
        return CPF;
    }

    public void setCPF(String CPF) {
        this.CPF = CPF;
    }

    public String getEndereco() {
        return Endereco;
    }

    public void setEndereco(String Endereco) {
        this.Endereco = Endereco;
    }

    public String getTelefone() {
        return Telefone;
    }

    public void setTelefone(String Telefone) {
        this.Telefone = Telefone;
    }

    public String getEmail() {
        return Email;
    }

    public void setEmail(String Email) {
        this.Email = Email;
    }

    public String getDataNasci() {
        return DataNasci;
    }

    public void setDataNasci(String DataNasci) {
        this.DataNasci = DataNasci;
    }

    public String getSerieAno() {
        return SerieAno;
    }

    public void setSerieAno(String SerieAno) {
        this.SerieAno = SerieAno;
    }

    public String getNomeRespo() {
        return NomeRespo;
    }

    public void setNomeRespo(String NomeRespo) {
        this.NomeRespo = NomeRespo;
    }

    public String getEscolaOrigem() {
        return EscolaOrigem;
    }

    public void setEscolaOrigem(String EscolaOrigem) {
        this.EscolaOrigem = EscolaOrigem;
    }
    

} 

   