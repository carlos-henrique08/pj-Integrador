    public ContasAPagar MontarContasAPagar(){
    ContasAPagar CAP = new ContasAPagar();
    CAP.setDescricao(txfDescricao.getText());
    CAP.setValor(txfValor.getText());
    CAP.setDataVencimento(txfDatavenci.getText());
    CAP.setDataPagamento(txfDatapagamento.getText());
    CAP.setFornecedor(txfFornecedor.getText());
    CAP.setCategoria(txfCategoria.getText());
    CAP.setStatusPagamento(txfStatusPagamento.getText());
    CAP.setMetodoPagamento(txfMetodopagamento.getText());
    CAP.setObservacoes(txfObservacoes.getText());
    
    return CAP;
    
    }
    
