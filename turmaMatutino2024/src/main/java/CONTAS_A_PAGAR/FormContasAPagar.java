    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed
            ContasAPagar CAP = this.MontarContasAPagar();
        if(CAP!= null && !txtFCodContasApagar_.getText().trim().isEmpty()){
           CAP.setCodContasAPagar(Integer.parseInt(txtFCodContasApagar_.getText()));
           ContasAPagar_Controle CPC = new ContasAPagar_Controle();
           CPC.update(CAP);
        }else{
            JOptionPane.showMessageDialog(null, "Preencha todos os Campos corretamentes");
            
        }
    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
         if(!txtFCodContasApagar_.getText().trim().isEmpty()){
            int Cod = Integer.parseInt(txtFCodContasApagar_.getText());
            ContasAPagar_Controle CAC = new ContasAPagar_Controle();
           CAC.delete(Cod);
        }else{
           JOptionPane.showMessageDialog(null, "Informe Novamente...");
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

