public class Produto {
 
 private String Nome_produto;
 private float Cod_produto;
 private String Descricao;
 private String marca_produto;
 private int Quantidade;
 private float preco_unitario;

    public String getNome_produto() {
        return Nome_produto;
    }

    public void setNome_produto(String Nome_produto) {
        this.Nome_produto = Nome_produto;
    }

    public float getCod_produto() {
        return Cod_produto;
    }

    public void setCod_produto(float Cod_produto) {
        this.Cod_produto = Cod_produto;
    }

    public String getDescricao() {
        return Descricao;
    }

    public void setDescricao(String Descricao) {
        this.Descricao = Descricao;
    }

    public String getMarca_produto() {
        return marca_produto;
    }

    public void setMarca_produto(String marca_produto) {
        this.marca_produto = marca_produto;
    }

    public int getQuantidade() {
        return Quantidade;
    }

    public void setQuantidade(int Quantidade) {
        this.Quantidade = Quantidade;
    }

    public float getPreco_unitario() {
        return preco_unitario;
    }

    public void setPreco_unitario(float preco_unitario) {
        this.preco_unitario = preco_unitario;
    }
 
 
 
    
    
    
}
